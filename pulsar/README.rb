# = Pulsar
# This is the base folder for the Pulsar prototype. 
# "Pulsar" is the codename for prototype3, the third iteration
# of the bayes-swarm extraction engine.
# 
# The concept behind the Pulsar prototype is to divide the execution
# chain into 'components'. Each component is responsible to handle 
# a particular task, such as extracting web pages, or consolidating them
# into a database.
# 
# This folder contains the code necessary to kickstart the execution
# of components. Components themselves are located in the component/ folder
